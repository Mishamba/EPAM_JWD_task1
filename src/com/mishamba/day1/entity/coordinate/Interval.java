package com.mishamba.day1.entity.coordinate;

public class Interval {
    private int a;
    private int b;

    public Interval(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int getA() {
        return this.a;
    }

    public int getB() {
        return this.b;
    }
}
